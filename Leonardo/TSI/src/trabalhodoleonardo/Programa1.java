    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Clicou SIM
        //VERIFICA SE O NODO ATUAL TEM DOIS FILHOS COM TEXTO, SE SIM PERCORRE COM BASE NA RESPOSTA E VAI PRA PROXIMA BRANCH
        //ELSE IF SE NAO TIVER FILHO DA ESQUERDA, VERIFICA
        //SEU CASO BASE EH: EH UMA RESPOSTA? JOGA SOBRE FORMA DE PERGUNTA, SE DER ERRADO TRATE A ARVORE E COMECE DENOVO
        //inicio do caso base
        if(atual.direita == null){
            System.out.println("Aqui caiu no caso bizarro, sei la o que aconteceu");
            String resposta = JOptionPane.showInputDialog(rootPane, "Informe agora o nome do seu animal");
            atual.direita = new Node(resposta, null, null);
            atual = nodoRoot;
            emCasoDeAcerto();
        }
        //Aqui caiu no caso do filho da direita ser uma resposta, ai manda-se sobre forma de pergunta
        else if(atual.verificaPergunta(atual.direita) == false){
            
            int reply = JOptionPane.showConfirmDialog(rootPane, "O seu animal eh o:"+ atual.direita.getTexto()+"?");
            
            if(reply == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(rootPane, "Acertei");
                atual = nodoRoot;
                emCasoDeAcerto();
            }
            
            else if(reply == JOptionPane.NO_OPTION){
                String pergunta = JOptionPane.showInputDialog(rootPane, "Escreva uma pergunta que diferencie o meu animal do seu:");
                String resposta = JOptionPane.showInputDialog(rootPane, "Informe agora o nome do seu animal");
                Node respostaCerta = new Node(pergunta, new Node(atual.direita.texto), new Node(resposta));
                atual.direita = respostaCerta;
                emCasoDeAcerto();
                atual = nodoRoot;
                //Reinicia o codigo com os botoes visiveis invisiveis e os que foram apagados voltam a ser vistos
                //reinicia o nodoAtual como root
            }
            else{
                //Caiu no Cancel, nao faz nada
            }
        }
        
        //Pergunta
        //Se cair aqui, o usuario clicou sim e tem uma pergunta, move o nodo atual para a direita e da display na pergunta
        else if(atual.verificaPergunta(atual.direita)){
            atual = atual.direita;
            atualizaTextoDaCaixa(atual.texto);
        }
        //fim do caso base  
    }//GEN-LAST:event_jButton1ActionPerformed

