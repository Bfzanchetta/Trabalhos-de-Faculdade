    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //comeca caso base 1
        if(atual.esquerda == null){
            String resposta = JOptionPane.showInputDialog(rootPane, "Informe agora o nome do seu animal");
            atual.esquerda = new Node(resposta, null, null);
            atual = nodoRoot;
            emCasoDeAcerto();
        }
        //No caso de baixo pega-se a resposta e lanca como pergunta
        else if(atual.verificaPergunta(atual.esquerda)==false){
            int reply2 = JOptionPane.showConfirmDialog(rootPane, "Por acaso o seu animal eh o:"+ atual.esquerda.getTexto() + "?");
            
            if(reply2 == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(rootPane, "Acertei");
                atual = nodoRoot;
                emCasoDeAcerto();
            }
            else if(reply2 == JOptionPane.NO_OPTION){
                String pergunta = JOptionPane.showInputDialog(rootPane, "Escreva uma pergunta que diferencie o meu animal do seu:");
                String resposta = JOptionPane.showInputDialog(rootPane, "Informe agora o nome do seu animal");
                Node esquerdo = new Node(pergunta, new Node(atual.esquerda.getTexto()), new Node(resposta));
                atual.esquerda = esquerdo;
                atual = nodoRoot;
                emCasoDeAcerto();
            }
            else{
                //Caiu no Cancel, nao faz nada
            }
        }
        
        //Nesse caso atualiza-se o atual e atualiza-se o JTextField
        else if(atual.verificaPergunta(atual.esquerda)){
            atual = atual.esquerda;
            atualizaTextoDaCaixa(atual.getTexto());
        }
        //fim do caso base
    }//GEN-LAST:event_jButton2ActionPerformed

